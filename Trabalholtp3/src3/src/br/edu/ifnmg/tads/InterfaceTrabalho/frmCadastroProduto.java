    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente adicionar o Produto?")== 0) {
            estoque = new Estoque();
            produto = new Produto();
            try{
                produto.setDescricao(txtDescricao.getText());
                produto.setValorCompra(Float.parseFloat(txtVlrCompra.getText()));
                produto.setValorVenda(Float.parseFloat(txtValorVenda.getText()));
                estoque.setQuantidade(Integer.parseInt(txtQtde.getText()));
                //estoque.setProdutos(produto);
            } catch (Exception ex){
                Logger.getLogger(frmCadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            int x = daoproduto.IdProduto();
            estoque = new Estoque(x, estoque.getQuantidade(), estoque.getIdEstoque());
            daoproduto.Salvar(produto);
            daestoque.Salvar(estoque);            
            JOptionPane.showMessageDialog(rootPane, "Produto adicionado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ação cancelada pelo usuário!");

        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

