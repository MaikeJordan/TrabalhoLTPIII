    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(rootPane, "Deseja realmente adicionar o Cliente?")
                == 0) {
            try {
                cliente.setNome(txtNome.getText());
                cliente.setCPF(txtCPF.getText());
                cliente.setRG(txtRG.getText());
                Date test = null;
                try {
                    test = formataData(txtDataNasc.getText());
                } catch (Exception ex) {
                    Logger.getLogger(FrmCadastroClient.class.getName()).log(Level.SEVERE, null, ex);
                }
                cliente.setNascimento(test);
                if (dao.Salvar(cliente)) {
                    int x = dao.IdPessoa();
                    cliente.setIdPessoa(x);
                    for (Email em : cliente.getEmails()) {
                        email = new Email(em.getIdEmail(), cliente, em.getNomeEmail());
                        daoEmail.Salvar(email);
                    }

                    for (Telefone tel : cliente.getTelefones()) {
                        telefone = new Telefone(tel.getNumero(), tel.getIdTelefone(), cliente);
                        daoTelefone.Salvar(telefone);
                    }

                    for (Endereco End : cliente.getEnderecos()) {
                        endereco = new Endereco(End.getEstado(), End.getCidade(), End.getBairro(), End.getRua(), End.getNumero(), cliente);
                        daoendereco.Salvar(endereco);
                    }
                }
            } catch (Exception ex) {
                Logger.getLogger(FrmCadastroClient.class.getName()).log(Level.SEVERE, null, ex);
            }
            dao.Salvar(cliente);
            JOptionPane.showMessageDialog(rootPane, "Cliente adicionado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "AÃ§Ã£o cancelada pelo usuÃ¡rio!");
            dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

