    void configuraTblEmail() {
        DefaultTableModel model = new DefaultTableModel();
        //model.addColumn("Id");
        model.addColumn("Email");
        for (Email em : cliente.getEmails()) {
            Vector v = new Vector();
            //  v.add(em.getIdEmail());
            v.add(em.getNomeEmail());
            model.addRow(v);
        }
        tblEmail.setModel(model);
    }

    void configuraTblTelefone() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Telefone");
        for (Telefone tel : cliente.getTelefones()) {
            Vector v = new Vector();
            v.add(tel.getNumero());
            model.addRow(v);
        }
        tblTelefone.setModel(model);

    }

    void configuraTblEndereco() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Rua");
        model.addColumn("Bairro");
        model.addColumn("Cidade");
        model.addColumn("Estado");
        model.addColumn("NÂº");
        for (Endereco ende : cliente.getEnderecos()) {
            Vector v = new Vector();
            v.add(ende.getRua());
            v.add(ende.getBairro());
            v.add(ende.getCidade());
            v.add(ende.getEstado());
            v.add(ende.getNumero());
            model.addRow(v);
        }
        tblEndereco.setModel(model);
    }

